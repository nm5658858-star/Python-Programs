print("Pattern via while loop")
i = 1
while(i<=5):                            #*
    j = 1                                      #**
    while(j<=i):                        #***
        print('*',end = "")            #****
        j = j + 1                            #*****
    print()    
    i = i + 1
print("End of program")
                                       


